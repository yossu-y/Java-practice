public class Chapter04 {
	public static void main(String[] args) {
		int num1;
		String str1;
		num1 = 10;
		str1 = "Hello World";
		System.out.println(num1);
		System.out.println(str1);
		
		num1 = 20;
		System.out.println(num1);
		
		int num2 = 30;
		System.out.println(num2);
		
		String word = "山田太郎";
		System.out.println(word);
		
		word = "一日生きることは、一歩進むことでありたい";
		System.out.println(word);
		
		final int number = 1;
		System.out.println(number);
		
		
	}
}
